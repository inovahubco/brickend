    /**
     * Generate Zod schema for a field
     */
    private generateZodSchema(field: Field): string {
        let schema = '';

        // Base type
        switch (field.type) {
            case 'string':
            case 'text':
            case 'email':
            case 'url':
            case 'uuid':
                schema = 'z.string()';
                break;
            case 'number':
            case 'integer':
            case 'float':
            case 'decimal':
                schema = 'z.number()';
                break;
            case 'boolean':
                schema = 'z.boolean()';
                break;
            case 'date':
            case 'datetime':
                schema = 'z.string().datetime()';
                break;
            case 'json':
                schema = 'z.any()';
                break;
            default:
                schema = 'z.string()';
        }

        // Add validation rules
        if (field.validation) {
            if (field.type === 'email') {
                schema += '.email()';
            }
            if (field.type === 'url') {
                schema += '.url()';
            }
            if (field.validation.minLength) {
                schema += `.min(${field.validation.minLength})`;
            }
            if (field.validation.maxLength) {
                schema += `.max(${field.validation.maxLength})`;
            }
            if (field.validation.min) {
                schema += `.min(${field.validation.min})`;
            }
            if (field.validation.max) {
                schema += `.max(${field.validation.max})`;
            }
            if (field.validation.pattern) {
                schema += `.regex(/${field.validation.pattern}/)`;
            }
            if (field.validation.enum) {
                schema += `.enum([${field.validation.enum.map(v => `"${v}"`).join(', ')}])`;
            }
        }

        // Make optional if not required
        if (!field.validation?.required || field.options?.nullable) {
            schema += '.optional()';
        }

        return schema;
    }

    /**
     * Check if field is filterable
     */
    private isFilterableField(field: Field): boolean {
        // Allow filtering on most field types except large text and json
        return !['text', 'json'].includes(field.type);
    }

    /**
     * Get table name for entity
     */
    private getTableName(entity: Entity): string {
        return entity.options?.tableName || stringUtils.pluralize(entity.name.toLowerCase());
    }
