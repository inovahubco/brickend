        code.push('');
        code.push('  // Handle custom application errors');
        code.push('  else if (error.statusCode) {');
        code.push('    statusCode = error.statusCode;');
        code.push('    errorCode = error.code || ErrorCode.INTERNAL_ERROR;');
        code.push('    message = error.message;');
        code.push('    details = error.details;');
        code.push('  }');
        code.push('');
        code.push('  const errorResponse: ErrorResponse = {');
        code.push('    error: {');
        code.push('      code: errorCode,');
        code.push('      message,');
        code.push('      details,');
        code.push('      timestamp: new Date().toISOString()');
        code.push('    }');
        code.push('  };');
        code.push('');
        code.push('  return new Response(JSON.stringify(errorResponse), {');
        code.push('    status: statusCode,');
        code.push('    headers: { "Content-Type": "application/json" }');
        code.push('  });');
        code.push('}');
        code.push('');

